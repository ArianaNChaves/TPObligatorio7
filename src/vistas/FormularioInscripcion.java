    private void mostrarDatos() {
        int cantidadAlumnos = Alumno.listAlumnos.size();
        DefaultComboBoxModel modelo = (DefaultComboBoxModel) jcboxAlumno.getModel();
        jcboxAlumno.setModel(modelo);
        for (int i = 0; i < cantidadAlumnos; i++) {
            modelo.addElement(listAlumnos.values());
        }

        int cantidadCursos = Materia.listaMaterias.size();
        DefaultComboBoxModel modelo2 = (DefaultComboBoxModel) jcboxMaterias.getModel();
        jcboxMaterias.setModel(modelo2);
        for (int i = 0; i < cantidadCursos; i++) {
            modelo2.addElement(Materia.listaMaterias.values());
        }
    }

