    private void btnInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInscribirActionPerformed
        Materia materia = (Materia) jcboxMaterias.getSelectedItem();
    Alumno alumno = (Alumno) jcboxAlumno.getSelectedItem();
    Inscripcion insc = new Inscripcion(alumno, materia);
    // Primero se pregunta si ya está matriculado
    if (!View.gestInsc.estaMatriculado(insc)) {
        View.gestInsc.agregarMatricula(insc);
        JOptionPane.showMessageDialog(null, "Matrícula agregada");
    } else {
        JOptionPane.showMessageDialog(null, "El alumno ya está matriculado en esta materia");
    }
    }//GEN-LAST:event_btnInscribirActionPerformed

